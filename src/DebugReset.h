#undef __FAIL
#define __FAIL(__ErrorSuffix__) \
    if (Result == RESOLVBUS_OK) { \
        Result = RESOLVBUS_ERROR_##__ErrorSuffix__; \
    } \


#undef __WRAP
#define __WRAP(__Expression__) \
    if (Result == RESOLVBUS_OK) { \
        Result = (__Expression__); \
    } \


#undef __ASSERT_WITH
#define __ASSERT_WITH(__ErrorSuffix__, __Expression__) \
    if (Result == RESOLVBUS_OK) { \
        if (!(__Expression__)) { \
            Result = RESOLVBUS_ERROR_##__ErrorSuffix__; \
        } \
    } \


